class Product {
  final String name;
  final String image;
  final num price;
  final String id;
  final num quantity;
  final List<String> sizes;
  final List<String> colors;

  Product({
    required this.name,
    required this.image,
    required this.price,
    required this.id,
    required this.quantity,
    required this.sizes,
    required this.colors,
  });
}
